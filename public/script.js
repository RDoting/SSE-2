$( document ).ready(function() {
    function calulateTo(amount, energyType) {
        var convArr = {"Joules": 1, "KWh": 3.6e6, 'Barrels of Oil': 6383087908.4, 'Calories': 4.18, "Kg of Uranium": 3.9e12, "Kg of Coal": 2.4e7, "Kg of Firewood": 1.6e7}
        var val_joules = amount * convArr[energyType] 
        calculateResults(val_joules)
        var val_all = []
        for (const [key, value] of Object.entries(convArr)) {
            if(key != energyType) {
                val_all.push({key: key, value: (val_joules / value)})
            }
        }
        return val_all
    }
    function calculateResults(amount) {
        var conversions = [
            {"name": "Microwave", "pre_name": "using a microwave for", "post_name": "seconds", "type": "Watts", "amount": 900}, 
            {"name": "Electric shower", "pre_name": "using an Electric shower for", "post_name": "seconds", "type": "Watts", "amount": 9000},
            {"name": "Train", "pre_name": "riding a train for", "post_name": "seconds", "type": "Watts", "amount": 2312000}, 
            {"name": "Rocket", "pre_name": "using a rocket for", "post_name": "seconds", "type": "Watts", "amount": 60000000000},
            {"name": "Fridge", "pre_name": "using a fridge for", "post_name": "seconds", "type": "Watts", "amount": 500}, 
            {"name": "Wind Turbine", "pre_name": "charging energy with a wind turbine for", "post_name": "seconds", "type": "Watts", "amount": 1000000},
            {"name": "Solar Panel", "pre_name": "charging energy with a solar panel for", "post_name": "seconds", "type": "Watts", "amount": 300}, 
            {"name": "F1 racecar", "pre_name": "driving an F1 racecar with 1050 HP for", "post_name": "seconds", "type": "Watts", "amount": 780000},
            {"name": "Steak", "pre_name": "eating ", "post_name": "Kg of a steak", "type": "Joules/KG", "amount": 9079280}, 
            {"name": "Rowing", "pre_name": "rowing for", "post_name": "seconds", "type": "Watts", "amount": 300}, 
            {"name": "Usain Bolt", "pre_name": "running as fast a Usain Bolt for", "post_name": "seconds", "type": "Watts", "amount": 2619}, 
            {"name": "TDF Cyclist", "pre_name": "cycling like a Tour de France Cyclist for", "post_name": "seconds", "type": "Watts", "amount": 450}, 
            {"name": "Airconditioning", "pre_name": "using an air conditioning unit for", "post_name": "seconds", "type": "Watts", "amount": 2000}, 
            {"name": "Cheetah", "pre_name": "running like a cheetah for", "post_name": "seconds", "type": "Watts", "amount": 500}, 
            {"name": "Sun", "pre_name": "energy generated by one cube in the sun for", "post_name": "seconds", "type": "Watts", "amount": 275}, 
        ]
        var htmlString = "";
        for(var i of conversions){
            var seconds = amount / i.amount;
            i.seconds = seconds
            var prioritize
            if(seconds > 1e8) {
                prioritize = 1
            } else if(seconds < 1 || seconds > 1e6) {
                prioritize = 2
            } else if( seconds > 1e4) {
                prioritize = 3
            } else{
                prioritize = 4
            }
            i.prioritize = prioritize;
        }
        const sorted_conversions = conversions.sort((a,b) => b.prioritize - a.prioritize);

        for(var i of sorted_conversions) {
            htmlString += '<div class="col mb-5">' +
                            '<div class="card" style="min-width: 200px; min-height: 150px; border-radius: 25px; border: 2px solid white; background-color: #327849">' +
                                '<div class="card-body">' +
                                    '<i class="fa fa-car float-right text-white"></i>' +
                                    '<h5 class="card-title text-white">' + i.name +'</h5>' +
                                    '<h6 class="card-subtitle text-white">At ' + i.amount + ' watts</h6>' +
                                    '<p class="card-text text-white">' + i.pre_name + " " + i.seconds.toFixed(2) + " " + i.post_name + '</p>' +
                                '</div>' +
                            '</div>' +
                           '</div>'
        }
        $('#examples').html(htmlString);
    }
    function reload() {
        var amount = $('#amount').val();
        var energyType = $('#energyType').val();
        var val = calulateTo(amount, energyType)
        var htmlString = "<div class='row mt-3 mb-3'>"
        for (const [key, value] of Object.entries(val)) {
            if(value.value > 1 && value.value < 1000) {
                htmlString = htmlString + 
                "<div class='col mb-3' >" +
                "<div class='d-flex align-items-center justify-content-center' style='color: white; min-height: 150px; border-radius: 25px; border: 2px solid white; background-color: #2596be;'>" + 
                value.value.toFixed(2) +
                " " + 
                value.key +
                "</div></div>"
            } else {
                htmlString = htmlString + 
                "<div class='col mb-3'>" +
                "<div class='d-flex align-items-center justify-content-center' style='color: white; min-height: 150px; border-radius: 25px; border: 2px solid white; background-color: #2596be;'>" + 
                value.value.toExponential(2) +
                " " + 
                value.key +
                "</div></div>"
                // '<div class="card" style="min-height: 200px;"><div class="card-body"><h5 class="card-title">Card Title</h5><p class="card-text">Some quick example text to build on the card.</p></div></div>'
            }
        }
        htmlString = htmlString + "</div>"
        $('#energy_results').html(htmlString);
    }
    $('#amount').on('input', function(e) {
        reload();
    })
    $('#energyType').on('change', function(e) {
        reload();
    })
});